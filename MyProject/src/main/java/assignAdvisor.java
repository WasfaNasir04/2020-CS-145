    public ArrayList ListAssign(String AdvisorName, String Code,Date DOB, String ProjectTitle)
    {
        ArrayList<Assigning_advisor> list = new ArrayList <Assigning_advisor>();
        Assigning_advisor AD = new Assigning_advisor(AdvisorName,Code,DOB,ProjectTitle);
        list.add(AD);
        return list;
        
    }
    
    public void addRow(String AdvisorName, String Code,Date DOB, String ProjectTitle){
        
        DefaultTableModel model= (DefaultTableModel) table_Assign.getModel();
        ArrayList<Assigning_advisor> list = ListAssign(AdvisorName,Code,DOB,ProjectTitle);
        Object rowData[] = new Object[4];
        for(int i = 0 ;i<list.size();i++){
        rowData[0] = list.get(i).AdvisorName;
        rowData[1] = list.get(i).Code;
        rowData[2] = list.get(i).DOB;
        rowData[3] = list.get(i).ProjectTitle;
        model.addRow(rowData);
    }
    }
    
