    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model= (DefaultTableModel) table_Assign.getModel();
        if(table_Assign.getSelectedRowCount()==1){
        String AdvisorName = ad_name.getText();
        String Code = grp_code.getText();
        Date DOB = duedate.getDate();
        String ProjectTitle = Pj_title.getText();
        
        
        table_Assign.setValueAt(AdvisorName, table_Assign.getSelectedRow(), 0);
        table_Assign.setValueAt(Code, table_Assign.getSelectedRow(), 1);
        table_Assign.setValueAt(DOB, table_Assign.getSelectedRow(), 2);
        table_Assign.setValueAt(ProjectTitle, table_Assign.getSelectedRow(), 3);
        
        JOptionPane.showMessageDialog(null,"Advisor has been changed/updated");
        }
        else{
        if(table_Assign.getRowCount()==0){
            JOptionPane.showMessageDialog(null,"Table is empty");
        }
        else{
            JOptionPane.showMessageDialog(null,"Select advisor!");
        }
        }
    }//GEN-LAST:event_updateActionPerformed

